"""
Problem:
    You are given an array nums consisting of non-negative integers. You are also given a queries array, where queries[i] = [xi, mi].

    The answer to the ith query is the maximum bitwise XOR value of xi and any element of nums that does not exceed mi. In other words, the answer is max(nums[j] XOR xi) for all j such that nums[j] <= mi. If all elements in nums are larger than mi, then the answer is -1.

    Return an integer array answer where answer.length == queries.length and answer[i] is the answer to the ith query.

    Example 1:
    Input: nums = [0,1,2,3,4], queries = [[3,1],[1,3],[5,6]]
    Output: [3,3,7]
    Explanation:
    1) 0 and 1 are the only two integers not greater than 1. 0 XOR 3 = 3 and 1 XOR 3 = 2. The larger of the two is 3.
    2) 1 XOR 2 = 3.
    3) 5 XOR 2 = 7.
    
    Example 2:
    Input: nums = [5,2,4,6,6,3], queries = [[12,4],[8,1],[6,3]]
    Output: [15,-1,5]

Solution:

Complexity:
    Time:
    Space:
"""


class TrieNode:
    def __init__(self, bits=0) -> None:
        self.children = {}
        self.bits = bits
        self.val = -1

    def add(self, n):
        node = self

        for i in range(self.bits - 1, -1, -1):
            bit = (n & (1 << i)) >> i

            if bit not in node.children:
                node.children[bit] = TrieNode()

            node = node.children[bit]

        node.val = n

    def search(self, n, limit):
        
        def dfs(node, i, bounded):

            if not node:
                return -1

            if i == 0:
                return node.val

            nBit = (n & (1 << i)) >> i
            limitBit = (limit & (1 << i)) >> i if not bounded else 1

            if limitBit == 0:
                
                return dfs(node.children[0], i - 1, bounded)
            else:
                if abs(nBit - 1) in node.children:
                    res = dfs(node.children[abs(nBit - 1)], i - 1, abs(nBit - 1) == 0)
                    if res == -1 and nBit in node.children:
                        res = dfs(node.children[nBit], i - 1, nBit == 0)
                    return res
                elif nBit in node.children:
                    return dfs(node.children[nBit], i - 1, nBit == 0)
                return -1

        return dfs(self, self.bits, False)

        node = self
        alreadyLesser = False

        for i in range(self.bits - 1, -1, -1):
            nBit = (n & (1 << i)) >> i
            limitBit = (limit & (1 << i)) >> i

            if alreadyLesser:
                limitBit = 1

            if limitBit == 0:
                if 0 in node.children:
                    node = node.children[0]
                else:
                    return -1

            else:
                if abs(nBit - 1) in node.children:
                    node = node.children[abs(nBit - 1)]
                    if abs(nBit - 1) == 0:
                        alreadyLesser = True
                elif nBit in node.children:
                    node = node.children[nBit]
                    if nBit == 0:
                        alreadyLesser = True
                else:
                    return -1

        return node.val


class Solution:
    def maximizeXor(self, nums: list[int], queries: list[list[int]]) -> list[int]:
        bits = max(
            queries[i][j] for i in range(len(queries)) for j in range(len(queries[0]))
        )
        bits = max(bits, max(nums))

        root = TrieNode(bits=bits.bit_length())

        for n in set(nums):
            root.add(n)

        res = []
        for t, l in queries:
            n = root.search(t, l)
            if n != -1:
                res.append(n ^ t)
            else:
                res.append(-1)

        return res


